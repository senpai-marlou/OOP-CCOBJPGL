package Attendance;

public class Book {
	 String book;
	 String author;
	 int copy;
	 
	public Book () {
		this.book = book;
		this.author = author;
		this.copy = copy;
	}
	
	
	public String getBook() {
		return book;
	}
	public void setBook(String book) {
		this.book = book;
	}
	public String getAuthor() {
		return author;
	}
	public void setAuthor(String author) {
		this.author = author;
	}
	public int getCopy() {
		return copy;
	}
	public void setCopy(int copy) {
		this.copy = copy;
	}
	
	
	 
	 
	 
}